import UIKit

var str = "Hello, playground"

// All variables in swift are of a specific type
// you can not change the type of variable after it has been defined
// I.E you cant ovewright a string into a int

var highScore = 100
var firstName = "Brad"
var isActive = true

// Declaring a var without a value
var myVariable : Int

// Basic data types
var myInt : Int
var myFloat : Float //32-bit floating point
var myDouble : Double // 64-bit floating point
var myString : String
var myChar : Character
var myBool : Bool // true/false

// These are also valid. We are telling the compiler the type of var
var lastName : String = "Allen"
var isOptional : Bool = false
var voltage : Int = 123

