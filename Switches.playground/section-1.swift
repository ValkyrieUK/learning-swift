// Playground - noun: a place where people can play

let windSpeed = 5

switch windSpeed {
case 0:
    println("The wind is calm")
case 1:
    println("A little breezy outside")
case 2:
    println("Hey its pretty windy")
case 3:
    println("Its windy out")
case 4..8
    println("OMG its so windy out !")
default:
    break
}